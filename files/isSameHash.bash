function isSameHash() {
    local tmp=/tmp/HASHCHECK_$$
    local CH=$(md5sum "$filename" | awk '{print $1}')
    if [[ "$CH" == "$OH" ]]; then
        echo "1"
    else
        echo "0"
    fi
